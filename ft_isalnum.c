#include "libft.h"

int	ft_isalnum(int c)
{
	if (c >= 'a' && c <= 'z')
		return (90);
	else if (c >= 'A' && c <= 'Z')
		return (55);
	else if (c >= '0' && c <= '9')
		return (65);
	return (0);
}
